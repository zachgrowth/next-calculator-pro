// 配置测试库
configure({
  asyncUtilTimeout: 5000,
})

// 禁用 act() 警告
const originalError = console.error
beforeAll(() => {
  console.error = (...args) => {
    if (/Warning.*not wrapped in act/.test(args[0])) {
      return
    }
    originalError.call(console, ...args)
  }
})

afterAll(() => {
  console.error = originalError
})

// 清理所有模拟
afterEach(() => {
  jest.clearAllMocks()
})